/**
 * Enum representing different types of graph layouts.
 * @enum {string}
 */
export enum LayoutType {
    /** Force-directed layout. */
    FR = 'FR',
    /** Circular layout. */
    CIRCLE = 'CIRCLE',
    /** Kamada-Kawai. */
    KK = 'KK',
    /** Self-Organizing Map layout */
    ISOM = 'ISOM',
}
