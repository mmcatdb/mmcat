package cz.matfyz.inference.schemaconversion.utils;

/**
 * Enum representing different types of graph layouts from the JUNG library.
 */
public enum LayoutType {
    /** Force-directed layout. */
    FR,

    /** Circular layout. */
    CIRCLE,

    /** Kamada-Kawai layout. */
    KK,

    /** Self-Organizing Map layout. */
    ISOM,
}
